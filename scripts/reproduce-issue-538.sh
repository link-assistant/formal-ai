#!/usr/bin/env bash
#
# Reproduce the issue #538 change on a *separate clean copy* of the repository,
# driven by the Agent CLI (`formal-ai agent`) — the issue's final acceptance
# check (requirement R24).
#
# What it proves:
#   * A pristine git work-tree of the branch tip (no local or dirty state)…
#   * …builds the `formal-ai` binary and drives three changes through the Agent
#     CLI with the *same task wording* recorded in the committed sessions — the
#     meaning-detail enrichment for BOTH concepts (tomato, potato) and the
#     generated agentic-recipe mermaid diagrams (a non-lexeme axis)…
#   * …and the freshly-generated Agent-CLI sessions match the committed
#     `docs/case-studies/issue-538/agent-cli-session*.json` byte-for-byte, and the
#     byte-for-byte seed-parity tests pass on the clean copy.
#
# Because the recipe itself is part of this PR, "a separate clean copy" means the
# branch's committed source (not `main`): the guarantee is that the exact data
# change is reproducible by the Agent CLI on a pristine checkout, with no hand
# editing anywhere in the loop.
#
# Usage:  scripts/reproduce-issue-538.sh
# Exit 0 on success (reproduced), non-zero otherwise.

set -euo pipefail

REPO_ROOT="$(git -C "$(dirname "$0")/.." rev-parse --show-toplevel)"
cd "$REPO_ROOT"

TOMATO_TASK="Make the tomato meaning more detailed: pin every surface's part of speech and grammatical number, ground it in Wikidata, and add the missing plural to томат."
POTATO_TASK="Please make the potato word and meaning richer — record the singular/plural of each surface, add the missing plural form potatoes, and keep it grounded in Wikidata."
DIAGRAM_TASK="Generate the mermaid diagrams of our agentic recipes, split into parts, as a visual overview of how Formal AI drives its own tools."

WORKTREE="$(mktemp -d "${TMPDIR:-/tmp}/issue-538-repro.XXXXXX")"
# Share the build cache with the main checkout so a second full target/ dir does
# not fill the disk (the issue asks us to be disk-frugal with the cargo cache).
export CARGO_TARGET_DIR="${CARGO_TARGET_DIR:-$REPO_ROOT/target}"

cleanup() {
  git worktree remove --force "$WORKTREE" >/dev/null 2>&1 || rm -rf "$WORKTREE"
}
trap cleanup EXIT

echo "==> Creating a separate clean work-tree at $WORKTREE (detached at HEAD)"
git worktree add --detach "$WORKTREE" HEAD >/dev/null
cd "$WORKTREE"

# A clean copy must have no uncommitted differences from the committed tree.
if [ -n "$(git status --porcelain)" ]; then
  echo "FAIL: the clean work-tree is unexpectedly dirty" >&2
  exit 1
fi

echo "==> Building the formal-ai binary in the clean copy"
cargo build --quiet --bin formal-ai

BIN="$CARGO_TARGET_DIR/debug/formal-ai"

fail=0
reproduce_one() {
  local label="$1" task="$2" committed="$3"
  local fresh="$WORKTREE/fresh-$label.json"
  echo "==> Driving the Agent CLI for '$label'"
  "$BIN" agent --task "$task" --session-json "$fresh" >/dev/null
  if diff -u "$REPO_ROOT/$committed" "$fresh" >/dev/null; then
    echo "    OK: $label session reproduces $committed byte-for-byte"
  else
    echo "    FAIL: $label session differs from $committed" >&2
    diff -u "$REPO_ROOT/$committed" "$fresh" | head -40 >&2 || true
    fail=1
  fi
}

reproduce_one tomato "$TOMATO_TASK" docs/case-studies/issue-538/agent-cli-session.json
reproduce_one potato "$POTATO_TASK" docs/case-studies/issue-538/agent-cli-session-potato.json
reproduce_one diagram "$DIAGRAM_TASK" docs/case-studies/issue-538/agent-cli-session-diagram.json

echo "==> Asserting byte-for-byte seed parity on the clean copy"
if cargo test --quiet --test unit -- issue_538_agentic >/dev/null 2>&1; then
  echo "    OK: issue_538_agentic tests pass (seed == Agent-CLI-driven output)"
else
  echo "    FAIL: issue_538_agentic tests failed on the clean copy" >&2
  fail=1
fi

if [ "$fail" -eq 0 ]; then
  echo "==> REPRODUCED: the issue #538 change is fully regenerated by the Agent CLI on a clean copy."
else
  echo "==> NOT reproduced (see failures above)." >&2
fi
exit "$fail"
