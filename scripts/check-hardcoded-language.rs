#!/usr/bin/env rust-script
//! Burn-down lint for hardcoded user-facing natural language in Rust source.
//!
//! "Data is the interface" (see `docs/design/no-hardcoded-natural-language.md`,
//! REQUIREMENTS R379): every user-facing sentence must come from the seed
//! knowledge base (`data/seed/*.lino`), not from a string literal typed into
//! the engine. This script scans `src/**/*.rs` for prose string literals and
//! compares the findings against an explicit allowlist
//! (`scripts/hardcoded-language-allowlist.txt`) that inventories today's debt.
//!
//! The check fails when:
//!   * a prose literal appears in `src/` that is **not** in the allowlist
//!     (new debt is blocked immediately), or
//!   * an allowlist row no longer occurs in `src/` (stale rows must be pruned
//!     as they are migrated into seed data, keeping the allowlist honest).
//!
//! Usage:
//!   rust-script scripts/check-hardcoded-language.rs           # check (CI/local)
//!   rust-script scripts/check-hardcoded-language.rs --write   # regenerate the
//!                                                             # allowlist from
//!                                                             # the current tree
//!
//! Run the inline unit tests with:
//!   rust-script --test scripts/check-hardcoded-language.rs
//!
//! ```cargo
//! [dependencies]
//! walkdir = "2"
//! ```

use std::collections::BTreeSet;
use std::fs;
use std::path::Path;
#[cfg(not(test))]
use std::process::exit;
use walkdir::WalkDir;

/// Relative path of the committed allowlist, from the repository root.
#[cfg_attr(test, allow(dead_code))]
const ALLOWLIST_PATH: &str = "scripts/hardcoded-language-allowlist.txt";

/// Directory scanned for Rust source. The web front-end has its own guard
/// (`tests/e2e/scripts/check-web-hardcoded-ui-strings.mjs`), so this lint owns
/// the Rust engine surfaces only.
const SCAN_DIR: &str = "src";

/// Header written at the top of a generated allowlist. Lines starting with `#`
/// are treated as comments and ignored when parsing.
const ALLOWLIST_HEADER: &str = "\
# Hardcoded natural-language allowlist (R379 burn-down).
#
# Generated by scripts/check-hardcoded-language.rs. Each row is a user-facing
# prose string literal that still lives in src/ instead of data/seed/*.lino.
#
# Format (tab-separated): <relative-path>\\t<canonical-literal-text>
#   * Control characters are escaped (\\n, \\t, \\r, \\\\); the whole row is one line.
#   * Rows are sorted for a stable, review-friendly diff.
#
# DO NOT add rows by hand to silence the check. Instead migrate the string into
# grounded meanings under data/seed/ (look it up via seed::response_for or a
# named vocabulary) and delete its row. Regenerate after a legitimate migration
# with: rust-script scripts/check-hardcoded-language.rs --write
";

/// A detected (or allowlisted) hardcoded literal: the file it lives in and the
/// canonical single-line form of the string.
#[derive(Debug, Clone, PartialEq, Eq, PartialOrd, Ord)]
struct Entry {
    file: String,
    text: String,
}

impl Entry {
    fn to_row(&self) -> String {
        format!("{}\t{}", self.file, self.text)
    }

    fn from_row(row: &str) -> Option<Entry> {
        let (file, text) = row.split_once('\t')?;
        if file.is_empty() || text.is_empty() {
            return None;
        }
        Some(Entry {
            file: file.to_string(),
            text: text.to_string(),
        })
    }
}

/// A raw string literal extracted from source, before the prose heuristic runs.
#[derive(Debug, PartialEq, Eq)]
struct RawLiteral {
    /// Canonical single-line text (control characters escaped).
    text: String,
}

/// Walk the source of a Rust file and extract every string literal, skipping
/// line comments (`// ...`, including doc comments) and block comments
/// (`/* ... */`). Handles normal strings (with escapes and `\`-newline
/// continuations) and raw strings (`r"..."`, `r#"..."#`).
fn extract_literals(content: &str) -> Vec<RawLiteral> {
    let chars: Vec<char> = content.chars().collect();
    let n = chars.len();
    let mut i = 0;
    let mut out = Vec::new();

    while i < n {
        let c = chars[i];

        // Line comment: `//` .. end of line (covers `///` and `//!` doc comments).
        if c == '/' && i + 1 < n && chars[i + 1] == '/' {
            while i < n && chars[i] != '\n' {
                i += 1;
            }
            continue;
        }

        // Block comment: `/* .. */` (non-nested is fine for skipping literals).
        if c == '/' && i + 1 < n && chars[i + 1] == '*' {
            i += 2;
            while i + 1 < n && !(chars[i] == '*' && chars[i + 1] == '/') {
                i += 1;
            }
            i += 2;
            continue;
        }

        // Character literal: skip so an apostrophe like `'"'` never opens a
        // spurious string. A char literal is a quote, one char (or escape),
        // and a closing quote.
        if c == '\'' {
            if i + 2 < n && chars[i + 1] == '\\' {
                // escaped char literal: '\n', '\'', '\\', ...
                let mut j = i + 2;
                while j < n && chars[j] != '\'' {
                    j += 1;
                }
                i = j + 1;
                continue;
            }
            if i + 2 < n && chars[i + 2] == '\'' {
                i += 3;
                continue;
            }
            // Otherwise it is a lifetime (`'a`) or similar; fall through.
            i += 1;
            continue;
        }

        // Raw string: r"...", r#"..."#, r##"..."##, ...
        if c == 'r' && i + 1 < n && (chars[i + 1] == '"' || chars[i + 1] == '#') {
            let mut j = i + 1;
            let mut hashes = 0;
            while j < n && chars[j] == '#' {
                hashes += 1;
                j += 1;
            }
            if j < n && chars[j] == '"' {
                j += 1;
                let start = j;
                // closing sequence: `"` followed by `hashes` `#`s
                let mut end = None;
                while j < n {
                    if chars[j] == '"' {
                        let mut k = j + 1;
                        let mut matched = 0;
                        while k < n && matched < hashes && chars[k] == '#' {
                            matched += 1;
                            k += 1;
                        }
                        if matched == hashes {
                            end = Some((j, k));
                            break;
                        }
                    }
                    j += 1;
                }
                if let Some((content_end, after)) = end {
                    let body: String = chars[start..content_end].iter().collect();
                    out.push(RawLiteral {
                        text: canonicalize_raw(&body),
                    });
                    i = after;
                    continue;
                }
                // Unterminated raw string: stop scanning this file safely.
                break;
            }
        }

        // Normal string literal.
        if c == '"' {
            let mut j = i + 1;
            let mut body = String::new();
            while j < n {
                let ch = chars[j];
                if ch == '\\' {
                    // keep the escape (or handle line continuation) verbatim
                    if j + 1 < n {
                        body.push('\\');
                        body.push(chars[j + 1]);
                        j += 2;
                        continue;
                    }
                    j += 1;
                    continue;
                }
                if ch == '"' {
                    break;
                }
                body.push(ch);
                j += 1;
            }
            out.push(RawLiteral {
                text: canonicalize_normal(&body),
            });
            i = j + 1;
            continue;
        }

        i += 1;
    }

    out
}

/// Escape a single character into the canonical single-line representation.
fn push_canonical_char(out: &mut String, ch: char) {
    match ch {
        '\n' => out.push_str("\\n"),
        '\t' => out.push_str("\\t"),
        '\r' => out.push_str("\\r"),
        '\\' => out.push_str("\\\\"),
        other => out.push(other),
    }
}

/// Canonicalize a raw-string body: no escapes, but real control characters are
/// escaped so the value stays on one line.
fn canonicalize_raw(body: &str) -> String {
    let mut out = String::new();
    for ch in body.chars() {
        push_canonical_char(&mut out, ch);
    }
    out
}

/// Canonicalize a normal-string body (escapes already captured verbatim). This
/// collapses `\`-newline line continuations (Rust elides the newline and the
/// following whitespace) and re-escapes any literal control characters, while
/// preserving explicit escapes such as `\n` and `\"` as their two-character
/// source form.
fn canonicalize_normal(body: &str) -> String {
    let chars: Vec<char> = body.chars().collect();
    let n = chars.len();
    let mut i = 0;
    let mut out = String::new();

    while i < n {
        let ch = chars[i];
        if ch == '\\' && i + 1 < n {
            let next = chars[i + 1];
            if next == '\n' {
                // Line continuation: drop the newline and following whitespace.
                i += 2;
                while i < n && (chars[i] == ' ' || chars[i] == '\t') {
                    i += 1;
                }
                continue;
            }
            // Preserve the escape as written (e.g. \n, \t, \", \\).
            out.push('\\');
            out.push(next);
            i += 2;
            continue;
        }
        push_canonical_char(&mut out, ch);
        i += 1;
    }

    out
}

/// A view of the canonical text with escapes rendered back to characters, used
/// only for the prose heuristic (so `\n` reads as a word break, not letters).
fn heuristic_view(text: &str) -> String {
    let chars: Vec<char> = text.chars().collect();
    let n = chars.len();
    let mut i = 0;
    let mut out = String::new();
    while i < n {
        if chars[i] == '\\' && i + 1 < n {
            match chars[i + 1] {
                'n' | 't' | 'r' => out.push(' '),
                '"' => out.push('"'),
                '\\' => out.push('\\'),
                other => out.push(other),
            }
            i += 2;
            continue;
        }
        out.push(chars[i]);
        i += 1;
    }
    out
}

/// Heuristic: is this literal a user-facing sentence rather than a code token,
/// identifier, format directive, or event/kind slug?
///
/// A literal qualifies when it reads as prose: it contains an ASCII space, ends
/// (ignoring trailing whitespace) with terminal punctuation (`.`, `!`, `?`),
/// and has at least two alphabetic words of two or more letters. Snake_case /
/// kebab-case tokens, single words, and placeholder-only strings do not match.
fn is_user_facing_prose(canonical: &str) -> bool {
    let view = heuristic_view(canonical);

    if !view.contains(' ') {
        return false;
    }

    let trimmed = view.trim_end();
    let ends_sentence = trimmed
        .chars()
        .last()
        .is_some_and(|c| c == '.' || c == '!' || c == '?');
    if !ends_sentence {
        return false;
    }

    // Count alphabetic words of length >= 2 (Unicode-aware: covers en/ru/hi).
    let mut words = 0;
    let mut current = 0;
    for ch in view.chars() {
        if ch.is_alphabetic() {
            current += 1;
        } else {
            if current >= 2 {
                words += 1;
            }
            current = 0;
        }
    }
    if current >= 2 {
        words += 1;
    }

    words >= 2
}

fn normalized_path(path: &Path) -> String {
    path.to_string_lossy()
        .replace(std::path::MAIN_SEPARATOR, "/")
}

fn relative_path(path: &Path, root: &Path) -> String {
    let relative = path.strip_prefix(root).unwrap_or(path);
    normalized_path(relative)
}

/// Scan `<root>/src/**/*.rs` and return the set of detected prose literals.
fn scan(root: &Path) -> BTreeSet<Entry> {
    let scan_root = root.join(SCAN_DIR);
    let mut entries = BTreeSet::new();

    for entry in WalkDir::new(&scan_root)
        .into_iter()
        .filter_map(std::result::Result::ok)
        .filter(|e| e.file_type().is_file())
    {
        let path = entry.path();
        if path.extension().and_then(|e| e.to_str()) != Some("rs") {
            continue;
        }

        let content = match fs::read_to_string(path) {
            Ok(content) => content,
            Err(error) => {
                eprintln!("Warning: could not read {}: {error}", path.display());
                continue;
            }
        };

        let file = relative_path(path, root);
        for literal in extract_literals(&content) {
            if is_user_facing_prose(&literal.text) {
                entries.insert(Entry {
                    file: file.clone(),
                    text: literal.text,
                });
            }
        }
    }

    entries
}

/// Parse a committed allowlist file into a set of entries (comments skipped).
fn parse_allowlist(content: &str) -> BTreeSet<Entry> {
    content
        .lines()
        .filter(|line| !line.trim_start().starts_with('#') && !line.trim().is_empty())
        .filter_map(Entry::from_row)
        .collect()
}

/// Render a set of entries as an allowlist file (header + sorted rows).
fn render_allowlist(entries: &BTreeSet<Entry>) -> String {
    let mut out = String::from(ALLOWLIST_HEADER);
    out.push('\n');
    for entry in entries {
        out.push_str(&entry.to_row());
        out.push('\n');
    }
    out
}

/// The two ways the committed allowlist can disagree with the source tree.
#[derive(Debug, PartialEq, Eq)]
struct Diff {
    /// Prose literals in src/ that are not allowlisted (new debt).
    new_violations: Vec<Entry>,
    /// Allowlist rows that no longer occur in src/ (stale, must be pruned).
    stale_entries: Vec<Entry>,
}

impl Diff {
    fn is_clean(&self) -> bool {
        self.new_violations.is_empty() && self.stale_entries.is_empty()
    }
}

fn diff(detected: &BTreeSet<Entry>, allowlist: &BTreeSet<Entry>) -> Diff {
    Diff {
        new_violations: detected.difference(allowlist).cloned().collect(),
        stale_entries: allowlist.difference(detected).cloned().collect(),
    }
}

#[cfg(not(test))]
fn write_mode(root: &Path) {
    let detected = scan(root);
    let allowlist_path = root.join(ALLOWLIST_PATH);
    let rendered = render_allowlist(&detected);
    match fs::write(&allowlist_path, &rendered) {
        Ok(()) => {
            println!(
                "Wrote {} entr{} to {}",
                detected.len(),
                if detected.len() == 1 { "y" } else { "ies" },
                ALLOWLIST_PATH
            );
        }
        Err(error) => {
            eprintln!("Failed to write {ALLOWLIST_PATH}: {error}");
            exit(1);
        }
    }
}

#[cfg(not(test))]
fn check_mode(root: &Path) {
    println!("\nChecking src/ for hardcoded user-facing natural language (R379)...\n");

    let detected = scan(root);
    let allowlist_path = root.join(ALLOWLIST_PATH);
    let allowlist = match fs::read_to_string(&allowlist_path) {
        Ok(content) => parse_allowlist(&content),
        Err(error) => {
            eprintln!(
                "::error::Could not read allowlist {ALLOWLIST_PATH}: {error}\n\
                 Generate it with: rust-script scripts/check-hardcoded-language.rs --write"
            );
            exit(1);
        }
    };

    let result = diff(&detected, &allowlist);

    println!(
        "Detected prose literals: {} | allowlisted: {}\n",
        detected.len(),
        allowlist.len()
    );

    if result.is_clean() {
        println!(
            "No new hardcoded natural language; allowlist is in sync ({} entries).\n",
            allowlist.len()
        );
        exit(0);
    }

    if !result.new_violations.is_empty() {
        println!(
            "New hardcoded user-facing strings found in src/ (not in the allowlist):\n"
        );
        for entry in &result.new_violations {
            println!("{}", annotation(entry));
            println!("  {}: \"{}\"", entry.file, entry.text);
        }
        println!(
            "\nMove each string into grounded meanings under data/seed/ and look it\n\
             up (e.g. via seed::response_for), or — only if it is genuinely part of\n\
             the existing debt — regenerate the allowlist with --write.\n"
        );
    }

    if !result.stale_entries.is_empty() {
        println!(
            "Stale allowlist rows (no longer present in src/ — prune them):\n"
        );
        for entry in &result.stale_entries {
            println!("  {}: \"{}\"", entry.file, entry.text);
        }
        println!(
            "\nThese strings were migrated or changed. Regenerate the allowlist with:\n\
             rust-script scripts/check-hardcoded-language.rs --write\n"
        );
    }

    exit(1);
}

#[cfg(not(test))]
fn annotation(entry: &Entry) -> String {
    let file = entry
        .file
        .replace('%', "%25")
        .replace('\r', "%0D")
        .replace('\n', "%0A")
        .replace(':', "%3A")
        .replace(',', "%2C");
    let message = format!(
        "Hardcoded user-facing string: \"{}\". Move it into data/seed/ (R379).",
        entry.text
    )
    .replace('%', "%25")
    .replace('\r', "%0D")
    .replace('\n', "%0A");
    format!("::error file={file}::{message}")
}

#[cfg(not(test))]
fn main() {
    let write = std::env::args().any(|arg| arg == "--write" || arg == "--update");
    let root = std::env::current_dir().expect("Failed to get current directory");

    if write {
        write_mode(&root);
    } else {
        check_mode(&root);
    }
}

#[cfg(test)]
mod tests {
    use super::*;
    use std::path::PathBuf;
    use std::time::{SystemTime, UNIX_EPOCH};

    fn temp_dir(name: &str) -> PathBuf {
        let nanos = SystemTime::now()
            .duration_since(UNIX_EPOCH)
            .unwrap()
            .as_nanos();
        let path = std::env::temp_dir().join(format!("check-hardcoded-language-{name}-{nanos}"));
        fs::create_dir_all(&path).unwrap();
        path
    }

    fn write_src(root: &Path, rel: &str, content: &str) {
        let path = root.join("src").join(rel);
        fs::create_dir_all(path.parent().unwrap()).unwrap();
        fs::write(path, content).unwrap();
    }

    #[test]
    fn detects_sentence_prose() {
        assert!(is_user_facing_prose("Sorry, I can't do that."));
        assert!(is_user_facing_prose("Hi, how may I help you?"));
        assert!(is_user_facing_prose("Test passed. I'm here."));
    }

    #[test]
    fn ignores_non_prose_literals() {
        // No terminal punctuation.
        assert!(!is_user_facing_prose("greeting"));
        assert!(!is_user_facing_prose("agent_suggestion_active"));
        // Single word with punctuation.
        assert!(!is_user_facing_prose("Done."));
        // Snake/kebab tokens and code-ish fragments.
        assert!(!is_user_facing_prose("physical_action_question"));
        assert!(!is_user_facing_prose("en ru hi zh"));
        // Placeholder-heavy format directive with no real words.
        assert!(!is_user_facing_prose("{} {}."));
    }

    #[test]
    fn continuation_lines_are_collapsed() {
        // `"a \<newline>   b."` reads as the logical sentence "a b."
        let literals = extract_literals("let s = \"Base case here \\\n            resolves it.\";");
        assert_eq!(literals.len(), 1);
        assert_eq!(literals[0].text, "Base case here resolves it.");
        assert!(is_user_facing_prose(&literals[0].text));
    }

    #[test]
    fn comments_are_not_scanned() {
        // A prose string inside a comment must not be extracted.
        let literals = extract_literals("// \"Sorry, I can't do that.\"\nlet x = 1;");
        assert!(literals.is_empty());
    }

    #[test]
    fn raw_strings_are_scanned() {
        let literals = extract_literals("let s = r#\"Sorry, I can't do that.\"#;");
        assert_eq!(literals.len(), 1);
        assert!(is_user_facing_prose(&literals[0].text));
    }

    #[test]
    fn fixture_string_is_a_new_violation() {
        // Acceptance criterion: introducing `"Sorry, I can't do that."` in src/
        // with an empty allowlist must be reported as a new violation.
        let root = temp_dir("fixture");
        write_src(
            &root,
            "solver.rs",
            "pub fn reply() -> &'static str {\n    \"Sorry, I can't do that.\"\n}\n",
        );

        let detected = scan(&root);
        let allowlist = BTreeSet::new();
        let result = diff(&detected, &allowlist);

        assert_eq!(
            result.new_violations,
            vec![Entry {
                file: "src/solver.rs".to_string(),
                text: "Sorry, I can't do that.".to_string(),
            }]
        );
        assert!(result.stale_entries.is_empty());
        assert!(!result.is_clean());
    }

    #[test]
    fn allowlisted_string_passes() {
        let root = temp_dir("allowlisted");
        write_src(
            &root,
            "solver.rs",
            "pub fn reply() -> &'static str {\n    \"Sorry, I can't do that.\"\n}\n",
        );

        let detected = scan(&root);
        let allowlist: BTreeSet<Entry> = [Entry {
            file: "src/solver.rs".to_string(),
            text: "Sorry, I can't do that.".to_string(),
        }]
        .into_iter()
        .collect();

        let result = diff(&detected, &allowlist);
        assert!(result.is_clean());
    }

    #[test]
    fn stale_allowlist_row_fails() {
        let root = temp_dir("stale");
        // The source no longer contains any prose; the allowlist still lists one.
        write_src(&root, "solver.rs", "pub fn n() -> u8 { 1 }\n");

        let detected = scan(&root);
        let allowlist: BTreeSet<Entry> = [Entry {
            file: "src/solver.rs".to_string(),
            text: "Sorry, I can't do that.".to_string(),
        }]
        .into_iter()
        .collect();

        let result = diff(&detected, &allowlist);
        assert!(result.new_violations.is_empty());
        assert_eq!(result.stale_entries.len(), 1);
        assert!(!result.is_clean());
    }

    #[test]
    fn allowlist_round_trips() {
        let entries: BTreeSet<Entry> = [
            Entry {
                file: "src/a.rs".to_string(),
                text: "Alpha sentence here.".to_string(),
            },
            Entry {
                file: "src/b.rs".to_string(),
                text: "Beta sentence with \\n break.".to_string(),
            },
        ]
        .into_iter()
        .collect();

        let rendered = render_allowlist(&entries);
        let parsed = parse_allowlist(&rendered);
        assert_eq!(entries, parsed);
    }
}
