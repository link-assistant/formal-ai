#!/usr/bin/env python3
"""Turn the gather progress journal into a clean, committed concepts file.

Reads `experiments/logs/gather_progress.jsonl`, keeps every grounded record,
prunes any whose slug or surface would collide with an existing seed meaning
(so the seed stays globally unique), drops internal duplicates, and writes a
deterministic `data/lexicon-import/common-nouns.lino` sorted by slug.
"""
import json
import os

ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
PROGRESS = os.path.join(ROOT, "experiments", "logs", "gather_progress.jsonl")
CONCEPTS = os.path.join(ROOT, "data", "lexicon-import", "common-nouns.lino")
LANGS = ["en", "ru", "hi", "zh"]


def load_set(name):
    path = os.path.join(ROOT, "experiments", "logs", name)
    if not os.path.exists(path):
        return set()
    return {line.strip() for line in open(path, encoding="utf-8") if line.strip()}


def main():
    existing_slugs = load_set("existing_slugs.txt")
    existing_surfaces = load_set("existing_surfaces.txt")

    kept = []
    for line in open(PROGRESS, encoding="utf-8"):
        line = line.strip()
        if not line:
            continue
        rec = json.loads(line)
        if not rec.get("kept"):
            continue
        kept.append(rec)

    seen_slugs, seen_surfaces = set(), set()
    final = []
    dropped = []
    for rec in kept:
        slug, qid, vals = rec["slug"], rec["qid"], rec["vals"]
        surfaces = [vals[lang] for lang in LANGS]
        reason = None
        if slug in existing_slugs:
            reason = "slug collides with existing meaning"
        elif slug in seen_slugs:
            reason = "duplicate slug in batch"
        elif any(s in existing_surfaces for s in surfaces):
            reason = "surface collides with existing seed surface"
        elif any(s in seen_surfaces for s in surfaces):
            reason = "surface duplicated in batch"
        if reason:
            dropped.append((slug, qid, reason))
            continue
        seen_slugs.add(slug)
        seen_surfaces.update(surfaces)
        final.append((slug, qid))

    final.sort(key=lambda pair: pair[0])

    os.makedirs(os.path.dirname(CONCEPTS), exist_ok=True)
    with open(CONCEPTS, "w", encoding="utf-8") as fh:
        fh.write("# Common concrete nouns grounded from Wikidata (issue #660, R378).\n")
        fh.write("# Generated by experiments/gather_common_nouns.py and pruned by\n")
        fh.write("# experiments/finalize_import.py; each line is `<slug> <Qid>` and\n")
        fh.write("# resolves to a committed entity cache record. Feed to\n")
        fh.write("# `formal-ai import lexemes --concepts <file> --offline`.\n")
        fh.write("concepts\n")
        for slug, qid in final:
            fh.write(f"  {slug} {qid}\n")

    print(f"kept {len(final)} concept(s); dropped {len(dropped)}")
    for slug, qid, reason in dropped:
        print(f"  drop {slug} {qid}: {reason}")


if __name__ == "__main__":
    main()
