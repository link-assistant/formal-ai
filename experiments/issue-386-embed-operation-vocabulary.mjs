// Issue #386 — give the JS worker the SAME operation-vocabulary canonicalization
// the Rust solver uses, so multilingual Python-synthesis requests (Hindi/Chinese/
// Russian "write/implement/return a function …") are recognised by *concept*, not
// by a hardcoded English-only gate.
//
// Rust's try_program_synthesis (src/solver_handlers/program_synthesis.rs) runs the
// normalized prompt through OperationVocabulary::canonicalized_prompt
// (src/seed/operation_vocabulary.rs) BEFORE gating/extracting: detect() substring-
// matches the per-language phrases in data/seed/operation-vocabulary.lino and
// APPENDS the canonical English token (e.g. native "लिखें।" -> appended " write"),
// which the boundary-aware role checks can then match. The JS worker never did
// this — and worse, it carried only a hand-maintained 3-operation subset
// (PROGRAM_MODIFIER_OPERATIONS) hardcoded in JS, a #386 smell.
//
// This one-shot, idempotent transform:
//   1. replaces the hardcoded PROGRAM_MODIFIER_OPERATIONS array with the FULL
//      operation vocabulary, embedded inline as OPERATION_VOCABULARY_LINO (byte-
//      identical to the canonical seed, regenerated by the sync script) and parsed
//      by operationVocabulary() — the JS mirror of operation_vocabulary();
//   2. adds detectOperations() + canonicalizedPrompt() — the JS mirror of
//      OperationVocabulary::detect / ::canonicalized_prompt;
//   3. wires canonicalizedPrompt() into tryProgramSynthesis, exactly like Rust;
//   4. repoints the two PROGRAM_MODIFIER_OPERATIONS call sites at operationVocabulary().
//
// Re-runnable: each step is guarded, so applying twice is a no-op.
//
// Run: node experiments/issue-386-embed-operation-vocabulary.mjs

import fs from "node:fs";

const root = new URL("..", import.meta.url);
const workerPath = new URL("src/web/formal_ai_worker.js", root);
const vocabPath = new URL("data/seed/operation-vocabulary.lino", root);

let worker = fs.readFileSync(workerPath, "utf8");

// House quote style (matches MEANINGS_LINO / PROGRAM_PLAN_RULES_LINO): a line that
// has a double-quote and neither single-quote nor backslash is wrapped in single
// quotes, otherwise it is JSON.stringify'd.
function jsString(line) {
  if (line.includes('"') && !line.includes("'") && !line.includes("\\")) {
    return "'" + line + "'";
  }
  return JSON.stringify(line);
}

function linoInlineBlock(constName, lino) {
  const lines = lino.replace(/\n+$/, "").split("\n");
  while (lines.length && lines[lines.length - 1] === "") lines.pop();
  const body = lines.map((l) => "  " + jsString(l)).join(",\n");
  return `const ${constName} = [\n${body},\n].join("\\n");`;
}

const vocabLino = fs.readFileSync(vocabPath, "utf8");

// --- step 1+2: replace the hardcoded array with the embedded LINO + parser ----
const arrayComment =
  "// Issue #358: program modifier recognition mirrors the Rust path: operation\n" +
  "// vocabulary trigger phrases provide natural-language evidence, and\n" +
  "// `program-plan-rules.lino` decides which operation slugs are valid modifiers.\n";
const arrayStart = arrayComment + "const PROGRAM_MODIFIER_OPERATIONS = [";

const replacement =
  `// Issue #386: the FULL multilingual operation vocabulary, embedded inline and
// byte-identical to data/seed/operation-vocabulary.lino (regenerated by
// experiments/issue-386-sync-worker-lexicon.mjs). The JS mirror of
// OperationVocabulary in src/seed/operation_vocabulary.rs: the CODE matches
// canonical concept tokens (write / function / similar_elements / reverse_sort …),
// while the per-language surface phrases live once here in the seed data — never
// a hardcoded per-language word list in code.
` +
  linoInlineBlock("OPERATION_VOCABULARY_LINO", vocabLino) +
  `

let cachedOperationVocabulary = null;
// Parse the embedded operation vocabulary into language-pooled triggers
// ({ slug, phrases, combos, inverse? }). Mirrors operation_vocabulary() in
// src/seed/operation_vocabulary.rs; phrases/combos are pooled across every
// language because operationFormMatches (below) is language-agnostic.
function operationVocabulary() {
  if (cachedOperationVocabulary) return cachedOperationVocabulary;
  const root = parseLinoTree(OPERATION_VOCABULARY_LINO);
  const container =
    root.children.find((child) => child.name === "operation_vocabulary") || root;
  const operations = [];
  for (const operationNode of container.children) {
    if (operationNode.name !== "operation") continue;
    const phrases = [];
    const combos = [];
    let inverse = null;
    for (const child of operationNode.children) {
      if (child.name === "inverse") inverse = child.value;
      else if (child.name === "language") {
        for (const form of child.children) {
          if (form.name === "phrase") phrases.push(form.value);
          else if (form.name === "combo") {
            combos.push(
              form.value
                .split("+")
                .map((token) => token.trim())
                .filter(Boolean),
            );
          }
        }
      }
    }
    const operation = { slug: operationNode.value, phrases, combos };
    if (inverse) operation.inverse = inverse;
    operations.push(operation);
  }
  cachedOperationVocabulary = operations;
  return cachedOperationVocabulary;
}`;

if (worker.includes("const OPERATION_VOCABULARY_LINO = [")) {
  // already embedded — leave the structural code in place (the sync script keeps
  // the LINO string itself byte-identical).
} else {
  const startIdx = worker.indexOf(arrayStart);
  if (startIdx === -1) throw new Error("anchor not found: PROGRAM_MODIFIER_OPERATIONS array + comment");
  const constIdx = worker.indexOf("const PROGRAM_MODIFIER_OPERATIONS = [", startIdx);
  const endMarker = "\n];";
  const endIdx = worker.indexOf(endMarker, constIdx);
  if (endIdx === -1) throw new Error("anchor not found: PROGRAM_MODIFIER_OPERATIONS array close");
  worker =
    worker.slice(0, startIdx) + replacement + worker.slice(endIdx + endMarker.length);
}

// --- step 3: add detectOperations + canonicalizedPrompt after operationFormMatches
const formMatchesFn = `function operationFormMatches(normalized, operation) {
  const source = String(normalized || "");
  return (
    (operation.phrases || []).some((phrase) => source.includes(phrase)) ||
    (operation.combos || []).some((combo) =>
      combo.every((token) => source.includes(String(token || ""))),
    )
  );
}`;
const canonicalHelpers = `

// Issue #386: every canonical operation token whose phrasing appears in
// \`normalized\`, in declaration order. Mirrors OperationVocabulary::detect in
// src/seed/operation_vocabulary.rs (substring match, so a native verb still
// matches when punctuation such as the danda \`।\` is glued to it).
function detectOperations(normalized) {
  const detected = [];
  for (const operation of operationVocabulary()) {
    if (operationFormMatches(normalized, operation)) detected.push(operation.slug);
  }
  return detected;
}

// Issue #386: append canonical English operation tokens to a normalized prompt so
// handlers keep matching canonical concepts while accepting native verbs from
// data/seed/operation-vocabulary.lino. Mirrors
// OperationVocabulary::canonicalized_prompt — additive (never removes text), so
// English prompts are unchanged and multilingual prompts gain boundary-matchable
// tokens (e.g. a Hindi "लिखें।" yields an appended " write").
function canonicalizedPrompt(normalized) {
  const detected = detectOperations(normalized);
  if (!detected.length) return String(normalized || "");
  let out = String(normalized || "");
  for (const canonical of detected) {
    out += \` \${canonical}\`;
    const phrase = canonical.replace(/_/g, " ");
    if (phrase !== canonical) out += \` \${phrase}\`;
  }
  return out;
}`;
if (!worker.includes("function canonicalizedPrompt(")) {
  if (!worker.includes(formMatchesFn)) throw new Error("anchor not found: operationFormMatches function");
  worker = worker.replace(formMatchesFn, formMatchesFn + canonicalHelpers);
}

// --- step 4: wire canonicalizedPrompt into tryProgramSynthesis ---------------
const synthHead = `function tryProgramSynthesis(prompt, normalized) {
  if (!looksLikePythonFunctionSynthesis(prompt, normalized)) return null;
  const functionName = extractPythonFunctionName(prompt, normalized);
  if (!functionName) return null;
  const candidate = synthesizePythonCandidate(prompt, normalized, functionName);`;
const synthHeadNew = `function tryProgramSynthesis(prompt, normalized) {
  // Issue #386: canonicalize first so native operation verbs (write / implement /
  // return …) in any supported language are recognised, exactly like
  // try_program_synthesis in src/solver_handlers/program_synthesis.rs.
  const canonical = canonicalizedPrompt(normalized);
  if (!looksLikePythonFunctionSynthesis(prompt, canonical)) return null;
  const functionName = extractPythonFunctionName(prompt, canonical);
  if (!functionName) return null;
  const candidate = synthesizePythonCandidate(prompt, canonical, functionName);`;
if (worker.includes(synthHead)) {
  worker = worker.replace(synthHead, synthHeadNew);
} else if (!worker.includes("const canonical = canonicalizedPrompt(normalized);")) {
  throw new Error("anchor not found: tryProgramSynthesis head");
}

// --- step 5: repoint PROGRAM_MODIFIER_OPERATIONS call sites ------------------
worker = worker.split("for (const operation of PROGRAM_MODIFIER_OPERATIONS) {").join(
  "for (const operation of operationVocabulary()) {",
);

// --- safety: the legacy identifier must be fully gone -----------------------
if (worker.includes("PROGRAM_MODIFIER_OPERATIONS")) {
  throw new Error("leftover reference to PROGRAM_MODIFIER_OPERATIONS");
}

fs.writeFileSync(workerPath, worker);
console.log("embedded operation vocabulary + canonicalizedPrompt into formal_ai_worker.js");
