//! Issue #499 agentic recipe — convert Google Trends into Formal AI requests.
//!
//! This is a generated-artifact recipe like the question catalog: the agentic loop
//! writes the deterministic benchmark fixture, verifies it by reading it back, and
//! returns the result. Live Google Trends access is a manual data-collection step;
//! the recipe consumes the committed RSS snapshot so CI and agent sessions are
//! reproducible.

use std::sync::OnceLock;

use crate::google_trends::{
    render_prompt_suite_from_rss, TrendPromptSuiteConfig, GOOGLE_TRENDS_US_RSS_URL,
};

/// The committed benchmark fixture generated by this recipe.
pub const TREND_PROMPT_CATALOG_PATH: &str = "data/benchmarks/google-trends-top10-suite.lino";

/// The canonical task wording that routes to this recipe.
pub const TREND_PROMPT_CATALOG_TASK: &str =
    "Convert the captured Google Trends top ten RSS snapshot into Formal AI request \
     cases, with one prompt variation in every supported language, and record the \
     generated prompt catalog as Links Notation.";

const RSS_SNAPSHOT: &str =
    include_str!("../../docs/case-studies/issue-499/raw-data/google-trends-us-rss.xml");

const TREND_PROMPT_CATALOG_KEYWORDS: [&str; 5] = [
    "google trends",
    "trending search",
    "trending searches",
    "trend prompt catalog",
    "trends top 10",
];

/// Whether `prompt` asks for the Google Trends prompt-catalog recipe.
#[must_use]
pub fn is_trend_prompt_catalog_task(prompt: &str) -> bool {
    let lower = prompt.to_lowercase();
    let has_trend_keyword = TREND_PROMPT_CATALOG_KEYWORDS
        .iter()
        .any(|keyword| lower.contains(keyword))
        || (lower.contains("trends") && (lower.contains("top 10") || lower.contains("top ten")));
    has_trend_keyword
        && (lower.contains("formal ai")
            || lower.contains("prompt")
            || lower.contains("request")
            || lower.contains("catalog"))
}

/// Render the canonical Google Trends prompt suite.
#[must_use]
pub fn render_document() -> String {
    static DOCUMENT: OnceLock<String> = OnceLock::new();
    DOCUMENT
        .get_or_init(|| {
            render_prompt_suite_from_rss(
                RSS_SNAPSHOT,
                &TrendPromptSuiteConfig {
                    geo: "US".to_owned(),
                    captured_at: "2026-07-08T20:29:32Z".to_owned(),
                    source_url: GOOGLE_TRENDS_US_RSS_URL.to_owned(),
                    source_snapshot:
                        "docs/case-studies/issue-499/raw-data/google-trends-us-rss.xml".to_owned(),
                    top_n: 10,
                },
            )
            .expect("committed issue-499 Google Trends RSS snapshot should render")
        })
        .clone()
}

/// The final answer for the generated-artifact recipe.
#[must_use]
pub fn final_answer(document: &str) -> String {
    format!(
        "Generated the Google Trends prompt catalog at {TREND_PROMPT_CATALOG_PATH}: \
         the captured US RSS top ten becomes 40 Formal AI request cases, one per \
         supported language (en, ru, hi, zh) for each trend. The committed RSS \
         snapshot keeps the source traceable while CI stays offline and deterministic.\n\n\
         Generated document ({TREND_PROMPT_CATALOG_PATH}):\n\n{}",
        document.trim_end(),
    )
}
