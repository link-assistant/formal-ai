//! The issue-#558 rebuild-and-reattach loop — recompiling Formal AI and reattaching the
//! improved worker to the UI (`R558-06`).
//!
//! Issue #558 asks that Formal AI be able to *"recompile and reattach the improved code
//! to the UI"* once a change is accepted. These pins lock that: the plan is derived from
//! an already-accepted change (a green gate AND a human approval), every reattached UI
//! artifact is grounded in the real repository bytes and the owned manifest, the pipeline
//! is ordered/observable/reversible, it stays human-gated and proposal-only, the plan
//! serialises to valid Links Notation with a stable content id, and the recipe is
//! reachable through the agentic interface as the eleventh recipe.

use formal_ai::agentic_coding::{
    is_rebuild_task, plan_chat_step, rebuild_plan as recipe, run_agentic_task, AgenticPlan,
    PlannedToolCall, DRIVER_TOOLS, REBUILD_PATH, REBUILD_TASK,
};
use formal_ai::change_request::canonical_change_request;
use formal_ai::learning_ledger::HumanApproval;
use formal_ai::self_improvement::BenchmarkGateReport;
use formal_ai::{canonical_rebuild_plan, owned_manifest, ChatMessage, RebuildPlan, ToolCall};
use lino_objects_codec::format::parse_indented;

fn expect_single_call(messages: &[ChatMessage], tools: &[&str]) -> PlannedToolCall {
    match plan_chat_step(messages, tools) {
        Some(AgenticPlan::ToolCalls(mut calls)) => {
            assert_eq!(calls.len(), 1, "the planner emits one call per step");
            calls.remove(0)
        }
        other => panic!("expected a single tool call, got {other:?}"),
    }
}

fn answer_tool_call(messages: &mut Vec<ChatMessage>, call: &PlannedToolCall, result: &str) {
    let id = format!("call_{}", messages.len());
    messages.push(ChatMessage::assistant_tool_calls(vec![ToolCall::function(
        id.clone(),
        call.tool.clone(),
        call.arguments.clone(),
    )]));
    messages.push(ChatMessage::tool_result(id, &call.tool, result));
}

/// An accepted change composed through the same human gate the ledger enforces, so the
/// rebuild plan can only ever follow an approved change.
fn accepted_change() -> formal_ai::change_request::AcceptedChange {
    let request = canonical_change_request();
    let green = BenchmarkGateReport::issue_362_from_counts(4, 0);
    request
        .review(&green, &HumanApproval::granted("maintainer"))
        .expect("the canonical change is green and approved, so it accepts")
}

#[test]
fn the_plan_is_derived_from_an_accepted_change() {
    // The core guarantee: a rebuild plan is a function of an *accepted* change (green gate
    // + human approval), so it carries that provenance and can never precede acceptance.
    let accepted = accepted_change();
    let plan = RebuildPlan::for_accepted_change(&accepted);
    assert_eq!(plan.change_id, accepted.change_id);
    assert_eq!(plan.requirement, accepted.requirement);
    assert_eq!(plan.reviewer, accepted.reviewer);
    assert!(plan.is_human_gated());
    assert_eq!(plan, canonical_rebuild_plan());
}

#[test]
fn every_reattached_artifact_is_grounded() {
    // Each UI artifact the rebuild reattaches is content-addressed from real bytes, and
    // the server entry's id matches the owned manifest — so the plan cannot reference a
    // fabricated artifact. The regenerated worker is deliberately absent: it is the
    // rebuild's *output*, referenced in the pipeline steps instead.
    let plan = canonical_rebuild_plan();
    let paths: Vec<_> = plan.artifacts.iter().map(|a| a.path.as_str()).collect();
    assert_eq!(
        paths,
        vec![
            "Cargo.toml",
            "src/main.rs",
            "src/web/formal_ai_worker.js",
            "src/web/index.html",
        ],
        "the reattached artifacts are the stable rebuild inputs, in a stable order"
    );
    for artifact in &plan.artifacts {
        assert!(
            !artifact.content_id.is_empty(),
            "every artifact is content-addressed"
        );
        assert!(!artifact.role.is_empty());
    }
    // The wasm output is not pinned as an input — it is regenerated by the pipeline.
    assert!(
        !plan
            .artifacts
            .iter()
            .any(|a| a.path == "src/web/formal_ai_worker.wasm"),
        "the regenerated worker is the rebuild output, not a grounded input"
    );
    // The server entry's content id is exactly what the owned manifest content-addresses.
    let manifest = owned_manifest();
    let server = plan
        .artifacts
        .iter()
        .find(|a| a.path == "src/main.rs")
        .expect("the server entry is reattached");
    let digest = manifest
        .iter()
        .find(|d| d.path == "src/main.rs")
        .expect("src/main.rs is owned");
    assert_eq!(
        server.content_id, digest.content_id,
        "the server entry id must match the owned manifest"
    );
}

#[test]
fn the_pipeline_is_ordered_observable_and_reversible() {
    // Every step of "recompile and reattach" is observable (proof it happened) and
    // reversible (how to roll it back), so keeping the swap stays a human decision.
    let plan = canonical_rebuild_plan();
    assert_eq!(
        plan.steps.len(),
        5,
        "recompile → worker → reattach → hot-swap → verify"
    );
    for (index, step) in plan.steps.iter().enumerate() {
        assert_eq!(step.ordinal, index + 1, "the pipeline is strictly ordered");
        assert!(!step.action.is_empty());
        assert!(!step.command.is_empty());
        assert!(
            !step.observable.is_empty(),
            "step {} must be observable",
            step.ordinal
        );
        assert!(
            !step.reversible.is_empty(),
            "step {} must be reversible",
            step.ordinal
        );
    }
    // The pipeline actually recompiles and reattaches the worker to the UI.
    let joined = plan
        .steps
        .iter()
        .map(|s| s.action.as_str())
        .collect::<Vec<_>>()
        .join(" ");
    assert!(joined.contains("Recompile"));
    assert!(joined.contains("Reattach"));
    assert!(joined.contains("worker"));
}

#[test]
fn plan_is_deterministic_and_content_addressed() {
    // Same accepted change → same plan and same content id.
    let a = RebuildPlan::for_accepted_change(&accepted_change());
    let b = RebuildPlan::for_accepted_change(&accepted_change());
    assert_eq!(a, b);
    assert_eq!(a.content_id(), b.content_id());
    assert!(a.id.starts_with("rebuild_plan_"));
}

#[test]
fn plan_renders_valid_links_notation() {
    let plan = canonical_rebuild_plan();
    let notation = plan.links_notation();
    parse_indented(&notation).expect("the plan should render valid Links Notation");
    assert!(notation.starts_with("rebuild_plan"));
    assert!(notation.contains("human_gated \"true\""));
    assert!(notation.contains("reattached_artifacts"));
    assert!(notation.contains("rebuild_and_reattach_pipeline"));
    assert!(notation.contains("observable"));
    assert!(notation.contains("reversible"));
    assert!(notation.contains("verification"));
}

#[test]
fn recognises_the_rebuild_task_without_colliding_with_the_sibling_recipes() {
    assert!(is_rebuild_task(REBUILD_TASK));
    assert!(is_rebuild_task(
        "Please reattach the improved worker to the UI."
    ));
    assert!(is_rebuild_task(
        "Rebuild the wasm worker and hot-swap the local server."
    ));
    // Unrelated and sibling requests do not route here.
    assert!(!is_rebuild_task("what files are in this folder?"));
    assert!(!is_rebuild_task("change this line to uppercase"));
    assert!(!is_rebuild_task(
        formal_ai::agentic_coding::self_heal::SELF_HEAL_TASK
    ));
    assert!(!is_rebuild_task(formal_ai::agentic_coding::CHANGE_TASK));
    assert!(!is_rebuild_task(
        formal_ai::agentic_coding::REPAIR_STRATEGY_TASK
    ));
    assert!(!is_rebuild_task(
        formal_ai::agentic_coding::source_graph::SOURCE_GRAPH_TASK
    ));
    // The rebuild task itself must not trip the sibling routers — in particular the
    // source-graph recipe, which owns "recompile" (absent here — this recipe keys on
    // "reattach").
    assert!(!formal_ai::agentic_coding::is_self_heal_task(REBUILD_TASK));
    assert!(!formal_ai::agentic_coding::is_self_ast_task(REBUILD_TASK));
    assert!(!formal_ai::agentic_coding::is_source_graph_task(
        REBUILD_TASK
    ));
    assert!(!formal_ai::agentic_coding::is_ledger_task(REBUILD_TASK));
    assert!(!formal_ai::agentic_coding::is_explain_task(REBUILD_TASK));
    assert!(!formal_ai::agentic_coding::is_change_request_task(
        REBUILD_TASK
    ));
    assert!(!formal_ai::agentic_coding::is_repair_strategy_task(
        REBUILD_TASK
    ));
}

#[test]
fn planner_walks_the_rebuild_recipe() {
    let tools = ["web_search", "web_fetch", "write_file", "run_command"];
    let mut messages = vec![ChatMessage::user(REBUILD_TASK)];

    // Step 1: write the generated rebuild-and-reattach plan (no web step — it is a pure
    // function of the accepted change and the grounded artifacts).
    let call = expect_single_call(&messages, &tools);
    assert_eq!(call.tool, "write_file");
    assert!(call.arguments.contains(REBUILD_PATH));
    let written: serde_json::Value = serde_json::from_str(&call.arguments).unwrap();
    assert_eq!(written["content"], recipe::render_document());
    answer_tool_call(&mut messages, &call, "wrote rebuild-and-reattach.lino");

    // Step 2: verify by reading the document back.
    let call = expect_single_call(&messages, &tools);
    assert_eq!(call.tool, "run_command");
    assert!(call.arguments.contains(REBUILD_PATH));
    answer_tool_call(&mut messages, &call, &recipe::render_document());

    // Step 3: the recipe is exhausted — the final answer carries the plan.
    match plan_chat_step(&messages, &tools) {
        Some(AgenticPlan::Final(answer)) => {
            assert!(answer.contains(REBUILD_PATH));
            assert!(answer.contains("reattaches it to"));
        }
        other => panic!("expected a final answer, got {other:?}"),
    }
}

#[test]
fn driver_drives_the_rebuild_recipe_to_a_write() {
    // End-to-end through the in-repo Agent CLI driver: the loop finishes and writes
    // exactly the generated rebuild-and-reattach plan.
    assert!(DRIVER_TOOLS.contains(&"write_file"));
    let outcome = run_agentic_task(REBUILD_TASK).expect("workspace");
    assert!(!outcome.hit_turn_cap, "the loop must finish, not run away");
    let write = outcome
        .steps
        .iter()
        .find(|step| step.tool == "write_file")
        .expect("a write step");
    let written: serde_json::Value = serde_json::from_str(&write.arguments).unwrap();
    assert_eq!(written["content"], recipe::render_document());
    assert_eq!(written["path"], REBUILD_PATH);
    assert!(outcome.final_answer.contains(REBUILD_PATH));
}
